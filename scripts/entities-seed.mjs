export function buildEntities(){
  const ents=[]; const labels=new Set();
  const ALL={...WORLDS, ...WORLDS_EXT};
  for(const [world,list] of Object.entries(ALL))
    for(const [label,importance,tags,related,desc] of list){
      if(labels.has(label)) continue; labels.add(label);
      ents.push({label,world,importance,tags,related,desc:DESC[label]||desc||null,...gem(label)});
    }
  // קשרים: related + קישור הדדי לפי תגית משותפת (בתוך הזרע)
  const edges=new Set();
  const E=(a,b)=>{ if(a!==b && labels.has(b)) edges.add(a+""+b); };
  for(const e of ents) for(const r of e.related) E(e.label,r);
  return {ents, edges:[...edges].map(s=>s.split(""))};
}

