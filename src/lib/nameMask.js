// זיהוי תבנית «פלוני בן/בת אלמוני» — שם אישי/תפילה (לרוב עם שם האם או האב).
// מחזיר את האינדקס של המילה «בן»/«בת» אם היא באמצע (יש מילה לפניה ואחריה), אחרת -1.
function benBatIdx(words) {
  for (let i = 1; i < words.length - 1; i++) {
    if (words[i] === "בן" || words[i] === "בת") return i;
  }
  return -1;
}

// האם הביטוי נראה כשם פרטי (שם פרטי מוכר בתחילה, «שם משפחה» בן שתי מילים, או «פלוני בן/בת אלמוני»)
export function looksLikeName(term) {
  const t = String(term || "").trim();
  if (!t || SAFE.has(t)) return false;
  const words = t.split(/\s+/);
  if (benBatIdx(words) >= 0) return true;                      // «X בן/בת Y» = שם אישי/תפילה — תמיד מסתירים
  if (words.length > 3) return false;                          // משפט ארוך = לא שם
  if (NAMES.has(words[0])) return true;                        // «שם פרטי ...» (כולל שם+משפחה)
  if (words.length === 2 && NAMES.has(words[1])) return true;  // «X משפחה-מוכרת» (שתי מילים בלבד)
  return false;
}

const head2 = w => { const h = [...String(w || "")].filter(c => HEB.test(c)).slice(0, 2).join(""); return (h || "★") + "★★★"; };

// ✶ זיהוי ג'יבריש — מילים שאי אפשר לקרוא תחבירית (מקשמוש/רצף א״ב/אות חוזרת/סופית באמצע).
// היוריסטיקה מבנית בלבד (בלי מילון) — כדי לא למסך שמות אמיתיים שאינם במאגר.
const GIB = "★★★";
const SOFIT = { "ך": "כ", "ם": "מ", "ן": "נ", "ף": "פ", "ץ": "צ" };
const ALEFBET = "אבגדהוזחטיכלמנסעפצקרשת";
const OK_PUNCT = /[\d׳״'"־\-]/;
const VOWEL = new Set(["א", "ה", "ו", "י", "ע"]);   // אותיות אם-קריאה (מטרי לקציוניס)
const HASFINAL = "כמנפצ";                            // אותיות שצורתן בסוף מילה חייבת להיות סופית

// מילה תחבירית? false = ג'יבריש (אם מילה אחת בביטוי נכשלת — כל הביטוי לא-קריא).
export function isReadable(term) {
  const t = String(term || "").trim();
  if (!t) return true;
  let zeroVowelTokens = 0, totalHeb = 0;
  for (const tok of t.split(/[\s\-־]+/)) {
    if (!tok) continue;
    if (/^\d+$/.test(tok)) continue;                                  // מספר טהור — קריא
    // אות לא-עברית שאינה ספרה/פיסוק מותר → לטינית/סקריפט אחר
    if ([...tok].some(c => !HEB.test(c) && !OK_PUNCT.test(c))) return false;
    const heb = [...tok].filter(c => HEB.test(c));
    if (!heb.length) continue;
    if (heb.length >= 14) return false;                               // אורך קיצון = מקשמוש
    for (let i = 0; i < heb.length; i++)                              // סופית שלא בסוף = פסול תחבירית
      if (SOFIT[heb[i]] && i !== heb.length - 1) return false;
    // אות שיש לה צורה סופית (כ/מ/נ/פ/צ) בסוף מילה = איות פסול (חייב ך/ם/ן/ף/ץ)
    if (heb.length >= 2 && HASFINAL.includes(heb[heb.length - 1])) return false;
    const s = heb.join("");
    if (/(.)\1\1/.test(s)) return false;                             // אות ×3 ברצף
    const norm = heb.map(c => SOFIT[c] || c);                         // רצף א״ב עולה ≥4
    let run = 1;
    for (let i = 1; i < norm.length; i++) {
      const a = ALEFBET.indexOf(norm[i - 1]), b = ALEFBET.indexOf(norm[i]);
      if (a >= 0 && b === a + 1) { if (++run >= 4) return false; } else run = 1;
    }
    const vowels = heb.filter(c => VOWEL.has(c)).length;
    if (heb.length >= 5 && vowels / heb.length < 0.2) return false;   // מילה ארוכה כמעט בלי תנועות = מקשמוש עיצורים
    if (heb.length >= 6 && new Set(norm).size / heb.length < 0.4) return false; // מעט אותיות שונות = חזרתיות (ובובבו)
    if (heb.length >= 2 && vowels === 0) zeroVowelTokens++;
    totalHeb += heb.length;
  }
  // ≥2 אסימוני-עיצורים בלי שום תנועה + אורך כולל ≥7 = ג'יבריש (כף פגם קעד) — בלי למסך "כל דם"/"בן גד"
  if (zeroVowelTokens >= 2 && totalHeb >= 7) return false;
  return true;
}

// מסכת ג'יבריש לתצוגה גולמית (קיר): לא-קריא → ★★★, אחרת המילה כמות שהיא.
export const maskGibberish = t => isReadable(t) ? t : GIB;

// מסכה: «פלוני בן/בת אלמוני» → מוחקים לגמרי את שם ההורה ומסתירים את הפרטי (2 אותיות).
//        שם פרטי + משפחה → רק השם הפרטי + כוכביות. שם בודד → 2 אותיות. אדמין → ללא מסכה.
//        ג'יבריש → ★★★ לכולם (כולל אדמין) — רעש, לא פרטיות.
export function maskTerm(term, isAdmin) {
  if (isAdmin) return term;   // 👑 אדמין רואה הכל — כולל ג'יבריש (בקשת צוריאל: «לראות הכל»)
  if (!isReadable(term)) return GIB;
  if (!looksLikeName(term)) return term;
  const words = String(term).trim().split(/\s+/);
  if (benBatIdx(words) >= 0) return head2(words[0]);   // פלוני בן/בת אלמוני → שם ההורה נמחק, הפרטי מוסתר
  if (words.length >= 2) return words[0] + " ★★★★★";   // שם + משפחה → רק השם הפרטי
  return head2(words[0]);                               // שם בודד → 2 אותיות
}

// יעד לינק בטוח: לשם מוסתר (לא-אדמין) או לג'יבריש → לפי הערך המספרי (לא חושף את המחרוזת ב-URL).
export function safeSearchHref(term, value, isAdmin) {
  if (isAdmin) return `/number/${encodeURIComponent(term)}`;   // 👑 אדמין → הביטוי המלא
  if (value != null && (!isReadable(term) || looksLikeName(term))) return `/number/${value}`;
  return `/number/${encodeURIComponent(term)}`;
}
